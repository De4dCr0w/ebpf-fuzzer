static inline int insn_add_one(struct bpf_insn *insns, int *idx, int max,
			struct bpf_insn insn)
{
	int start = *idx;
	int end = start + 1;
	if (end > max) {
		return -1;
	}

	insns[start] = insn;
	*idx = end;
	return 0;
}

static inline int insn_add(struct bpf_insn *insns, int *idx, int max,
		    struct bpf_insn *src_insn, int src_insn_count)
{
	int err = 0;

	for (int i = 0; i < src_insn_count; i++) {
		err = insn_add_one(insns, idx, max, src_insn[i]);
		if (err < 0)
			return -1;
	}

	return 0;
}

static inline int insn_get_map_ptr(struct bpf_insn *insns, int *idx, int max,
				  int fd, int reg)
{
	struct bpf_insn map_p[] = {
		EBPF_INSN_GET_MAP_ADDR(fd, reg),
	};

	return insn_add(insns, idx, max, map_p, ARRAY_CNT(map_p));
}

#define	COPY_INSNS(dst, IDX, src) \
({\
	int err = 0;\
	struct bpf_insn ____insns[] = {src};\
	err = insn_add(dst, (int *)&IDX, BPF_MAXINSNS, ____insns, ARRAY_CNT(____insns));\
	if (err < 0) \
		return -1;\
})

